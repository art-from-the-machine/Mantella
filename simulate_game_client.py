    def get_player_state_as_event(self) -> str:
        """Generate natural language player state event like Papyrus GetPlayerStateAsEvent().
        Returns empty string if state hasn't changed significantly.
        Format: "The player is sneaking, heavily wounded & heavily irradiated"
        """
        state_items = []
        
        # Build list of significant state items
        if self.PLAYER_STATE["power_armor"]:
            state_items.append("in power armor")
        if self.PLAYER_STATE["overencumbered"]:
            state_items.append("overencumbered")
        if self.PLAYER_STATE["sneaking"]:
            state_items.append("sneaking")
        if self.PLAYER_STATE["bleeding_out"]:
            state_items.append("bleeding out")
        
        # Health status
        health = self.PLAYER_STATE["health_percent"]
        if health < 0.4:
            state_items.append("heavily wounded")
        elif health < 0.7:
            state_items.append("moderately wounded")
        elif health < 0.9:
            state_items.append("lightly wounded")
        
        # Radiation status
        rad = self.PLAYER_STATE["rad_percent"]
        if rad > 0.6:
            state_items.append("heavily irradiated")
        elif rad > 0.3:
            state_items.append("moderately irradiated")
        elif rad > 0.15:
            state_items.append("lightly irradiated")
        
        # Combat state
        if self.PLAYER_STATE["in_combat"]:
            state_items.append("in combat")
        elif self.PLAYER_STATE["searching"]:
            state_items.append("alert and searching")
        
        # Build state key for change detection
        state_key = "".join(state_items)
        
        # Check if state changed
        if state_key == self._last_player_state_key:
            return ""  # No change
        self._last_player_state_key = state_key
        
        # Build natural language result
        if not state_items:
            return ""
        
        if len(state_items) == 1:
            return f"The player is {state_items[0]}"
        elif len(state_items) == 2:
            return f"The player is {state_items[0]} & {state_items[1]}"
        else:
            return f"The player is {', '.join(state_items[:-1])} & {state_items[-1]}"
    
