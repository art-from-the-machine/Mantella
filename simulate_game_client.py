    def handle_action(self, action_id: str, action_args: dict) -> list:
        """
        Simulate game behavior for an action.
        Returns list of events that would be generated by the game.
        
        This mimics what MantellaConversation.psc TriggerCorrectCustomEvent does.
        """
        events = []
        sources = action_args.get("source", [])
        npc_name = sources[0] if sources else "Unknown"
        
        print(f"   [TRACE] ACTION: {action_id} from {npc_name}")
        
        if action_id == "mantella_reload_conversation":
            events.append("Reloading conversation...")
            print(f"   ✓ GAME: Reloading conversation")
            
        elif action_id == "mantella_end_conversation":
            events.append("Ending conversation...")
            print(f"   ✓ GAME: Conversation ended by NPC")
            
        elif action_id == "mantella_remove_character":
            events.append(f"{npc_name} left the conversation")
            print(f"   ✓ GAME: {npc_name} left conversation")
            
        elif action_id == "mantella_npc_offended":
            if self.allow_aggro:
                self.hostile_npcs.add(npc_name)
                self.DANGER_CONTEXT["in_combat"] = True
                self.PLAYER_STATE["in_combat"] = True
                events.append(f"{npc_name} is hostile!")
                print(f"   ⚔ GAME: {npc_name} is now hostile, combat started")
            else:
                print(f"   ✗ GAME: Aggro blocked (MCM: allowActionAggro=false)")
                print(f"   [TRACE] ACTION REJECTED: Aggro disabled in MCM")
                
        elif action_id == "mantella_npc_forgiven":
            events.append(f"{npc_name} calmed down")
            if npc_name in self.hostile_npcs:
                self.hostile_npcs.discard(npc_name)
                print(f"   ☮ GAME: {npc_name} is no longer hostile")
            if not self.hostile_npcs:
                self.DANGER_CONTEXT["in_combat"] = False
                self.PLAYER_STATE["in_combat"] = False
                events.append("Combat has ended")
                
        elif action_id == "mantella_npc_follow":
            # Simulate MantellaConversation.psc follow logic exactly
            if not self.allow_follow:
                print(f"   ✗ GAME: Follow blocked (MCM: allowFollow=false)")
                print(f"   [TRACE] ACTION REJECTED: Follow disabled in MCM")
            else:
                # Check companion faction membership
                faction_rank = self.companion_faction_npcs.get(npc_name, -1) if self.companion_faction_npcs else -1
                is_companion = faction_rank >= 0
                
                if is_companion and not self.allow_npcs_stay_in_place:
                    print(f"   ✗ GAME: {npc_name} is already a companion")
                    print(f"   [TRACE] ACTION REJECTED: {npc_name} already in CompanionFaction (rank={faction_rank}), allowNPCsStayInPlace=false")
                else:
                    # Success
                    self.following_npcs.add(npc_name)
                    events.append(f"{npc_name} is following")
                    print(f"   ✓ GAME: {npc_name} is following")
                    print(f"   [TRACE] ACTION SUCCESS: {npc_name} now following")
                    
        elif action_id == "mantella_npc_inventory":
            if self.allow_inventory:
                events.append(f"{npc_name} inventory opened")
                print(f"   ✓ GAME: {npc_name} inventory opened")
            else:
                print(f"   ✗ GAME: Inventory blocked (MCM: allowActionInventory=false)")
                print(f"   [TRACE] ACTION REJECTED: Inventory disabled in MCM")
        
        elif action_id == "mantella_npc_unfollow":
            if npc_name in self.following_npcs:
                self.following_npcs.discard(npc_name)
                events.append(f"{npc_name} stopped following")
                print(f"   ✓ GAME: {npc_name} stopped following")
                print(f"   [TRACE] ACTION SUCCESS: {npc_name} stopped following")
            else:
                print(f"   ℹ GAME: {npc_name} was not following")
        
        elif action_id == "mantella_npc_wait":
            events.append(f"{npc_name} will wait")
            print(f"   ✓ GAME: {npc_name} will wait")
            print(f"   [TRACE] ACTION SUCCESS: {npc_name} waiting")
        
        elif action_id == "mantella_npc_moveto":
            target = action_args.get("target", "unknown")
            events.append(f"{npc_name} is moving towards {target}")
            print(f"   ✓ GAME: {npc_name} moving towards {target}")
            print(f"   [TRACE] ACTION SUCCESS: {npc_name} moving to {target}")
        
        elif action_id == "mantella_npc_loot":
            mode = action_args.get("mode", "any")
            events.append(f"{npc_name} will scavenge {mode} items")
            print(f"   ✓ GAME: {npc_name} scavenging {mode} items")
            print(f"   [TRACE] ACTION SUCCESS: {npc_name} looting {mode}")
        
        elif action_id == "mantella_heal_me":
            events.append(f"{npc_name} will heal the player")
            print(f"   💉 GAME: {npc_name} healing player with stimpak")
            print(f"   [TRACE] ACTION SUCCESS: {npc_name} healing player")
        
        elif action_id == "mantella_npc_use_item_on_target":
            target = action_args.get("target", "unknown")
            item_type = action_args.get("mode", "stimpak")
            events.append(f"{npc_name} using {item_type} on {target}")
            print(f"   💉 GAME: {npc_name} using {item_type} on {target}")
            print(f"   [TRACE] ACTION SUCCESS: {npc_name} using {item_type} on {target}")
        
        elif action_id == "mantella_npc_attack_other_npc":
            target = action_args.get("target", "unknown")
            events.append(f"{npc_name} is attacking {target}")
            self.DANGER_CONTEXT["in_combat"] = True
            self.PLAYER_STATE["in_combat"] = True
            print(f"   ⚔ GAME: {npc_name} attacking {target}")
            print(f"   [TRACE] ACTION SUCCESS: {npc_name} attacking {target}")
                
        else:
            print(f"   ℹ GAME: Action '{action_id}' acknowledged (no special handling)")
        
        # Store events for next context update
        self.pending_action_events.extend(events)
        return events
    
